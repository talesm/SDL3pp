#define SDL_KMOD_NUM 0x1000u

/// the Caps Lock key is down.
#define SDL_KMOD_CAPS 0x2000u

/// the !AltGr key is down.
#define SDL_KMOD_MODE 0x4000u

/// the Scroll Lock key is down.
#define SDL_KMOD_SCROLL 0x8000u

